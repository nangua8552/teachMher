package com.lq.school.teacher.dao.vo;

public class Teacher {
		private int id ;
	    private String name;
	    private int age;
	    private String sex ;
	    private String lev ;
	    
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
		public String getSex() {
			return sex;
		}
		public void setSex(String sex) {
			this.sex = sex;
		}
		public String getLev() {
			return lev;
		}
		public void setLev(String lev) {
			this.lev = lev;
		}
	

}
